var engWordIndex = 0;
var chinWordIndex = 0;

var score = 0;

var timeleft = 30;
var countdownTimer = setInterval("timer()", 1000);


//display time
function timer()
{
	document.getElementById("time").innerHTML = timeleft;
	if (timeleft == 0)
	{
	    clearInterval(countdownTimer);
	    endGame();
	}
	timeleft--;
}


//display score
function newScore()
{
	document.getElementById("score").innerHTML = score + 1;
	score++;
}

//display hints
function printClick()
{
	document.getElementById("hintContainer").innerHTML = "Click Me > <";
}

function printHint()
{
	document.getElementById("hintContainer").innerHTML = "Hint: move the cursor here"
}

function displayHint()
{
	engWordIndex = chinWordIndex;
	document.getElementById("hintContainer").innerHTML = english1[engWordIndex];
	
	var oldStr = document.getElementById("hintContainer").innerHTML;
	var newStr = oldStr.replace(/a|e|i|o|u/g, "*");
	document.getElementById("hintContainer").innerHTML = newStr;
}


//display words
function getRandomIndex()
{
	var random_num = Math.random() * chinese1.length;
	var random_int = Math.floor(random_num);
	return random_int;
}

function toStart()
{
	chinWordIndex = getRandomIndex();
	document.getElementById("wordContainer").innerHTML = chinese1[chinWordIndex];
}

function enterAnswer()
{
	engWordIndex = chinWordIndex;
	var answer = document.getElementById("input_engAnswer").value;
	var correctAnswer = english1[engWordIndex];
	var chinese = chinese1[chinWordIndex];

	if (answer == correctAnswer)
	{
		timeleft = 30;
		document.getElementById("feedbackContainer").innerHTML = "Correct! " + `<button id="nextWord" onclick="toNextWord()">Next Word</button>`;
		newScore();
	}
	else
	{
		document.getElementById("feedbackContainer").innerHTML = "The correct spelling of " + chinese + " is " + correctAnswer;
	}

	document.getElementById("input_engAnswer").value = null;
}

//display next word
function toNextWord()
{
	chinWordIndex = getRandomIndex();
	document.getElementById("wordContainer").innerHTML = chinese1[chinWordIndex];
}


//display End Game Element
function endGame()
{
	var endGameElement = document.getElementById("endGameArea");
	endGameElement.innerHTML = `
        <h1>Time ran out!</h1>
        <p>Your final score is ${score}</p>
        <br />
        <button id="nextLevel" onclick="toQuiz()">Next Level</button>`;

	endGameElement.style.display = "flex";
}


